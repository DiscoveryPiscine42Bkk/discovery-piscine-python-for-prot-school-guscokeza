#!/usr/bin/env python3

# Ask the user to input their age
age = int(input("Please enter your age: "))

# Calculate the age in 10, 20, and 30 years
age_in_10_years = age + 10
age_in_20_years = age + 20
age_in_30_years = age + 30

# Display the results
print(f"In 10 years, you will be {age_in_10_years} years old.")
print(f"In 20 years, you will be {age_in_20_years} years old.")
print(f"In 30 years, you will be {age_in_30_years} years old.")
